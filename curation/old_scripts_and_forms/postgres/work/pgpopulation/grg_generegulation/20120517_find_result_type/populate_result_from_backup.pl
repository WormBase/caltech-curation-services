#!/usr/bin/perl -w

# populate re-created grg_result tables from backup .pg dump.  2012 05 18

use strict;
use diagnostics;
use DBI;
use Encode qw( from_to is_utf8 );

my $dbh = DBI->connect ( "dbi:Pg:dbname=testdb", "", "") or die "Cannot connect to database!\n"; 
my $result;

my @pgcommands;

my %validTypes;
$validTypes{'Positive_regulate'}++;
$validTypes{'Negative_regulate'}++;
$validTypes{'Does_not_regulate'}++;

my %nameToPgid;
$result = $dbh->prepare( "SELECT * FROM grg_name" );
$result->execute() or die "Cannot prepare statement: $DBI::errstr\n"; 
while (my @row = $result->fetchrow) { $nameToPgid{$row[1]} = $row[0]; }

my $infile = 'from_backup_postgres';
open (IN, "<$infile") or die "Cannot open $infile : $!";
while (my $line = <IN>) {
  chomp $line;
  my ($name, $type) = split/\t/, $line;
  if ($validTypes{$type}) {
      1; }
    else { print "ERR invalid type for $line\n"; next; }
  if ($nameToPgid{$name}) {
      1; }
    else { print "ERR no pgid for $name\n"; next; }
  my $pgid = $nameToPgid{$name};
  push @pgcommands, "INSERT INTO grg_result VALUES ('$pgid', '$type')";
  push @pgcommands, "INSERT INTO grg_result_hst VALUES ('$pgid', '$type')";
} # while (my $line = <IN>)
close (IN) or die "Cannot close $infile : $!";

foreach my $command (@pgcommands) {
  print "$command\n";
# UNCOMMENT TO POPULATE
#   $dbh->do( $command );
} # foreach my $command (@pgcommands)

__END__


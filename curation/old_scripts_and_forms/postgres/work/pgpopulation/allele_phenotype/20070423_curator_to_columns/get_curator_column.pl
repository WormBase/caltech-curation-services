#!/usr/bin/perl -w

# Take the file from a COPY alp_curator TO file and add columns to those that
# have alp_terms.  2007 04 23

use strict;
use diagnostics;
use Pg;

my $conn = Pg::connectdb("dbname=testdb");
die $conn->errorMessage unless PGRES_CONNECTION_OK eq $conn->status;

my $infile = 'alp_curator_box_no_column';
my $outfile = 'alp_curator_box_and_column';
open (OUT, ">$outfile") or die "Cannot open $outfile : $!";
open (IN, "<$infile") or die "Cannot open $infile : $!";
while (my $line = <IN>) {
  my ($joinkey, $box, $data, $time) = split/\t/, $line;
  my $result = $conn->exec( "SELECT * FROM alp_term WHERE joinkey = '$joinkey' AND alp_box = '$box';" );
  my %columns;
  while (my @row = $result->fetchrow) {
    if ($row[3]) { $columns{$row[2]}++; } else { delete $columns{$row[2]}; } }
  foreach my $col (sort {$a<=>$b} keys %columns) { print OUT "$joinkey\t$box\t$col\t$data\t$time"; }
} # while (my $line = <IN>)

__END__


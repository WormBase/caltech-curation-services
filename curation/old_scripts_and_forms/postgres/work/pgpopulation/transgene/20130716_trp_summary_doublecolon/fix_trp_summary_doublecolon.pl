use strict;
use diagnostics;
use DBI;
use Encode qw( from_to is_utf8 );

my $dbh = DBI->connect ( "dbi:Pg:dbname=testdb", "", "") or die "Cannot connect to database!\n"; 
my $result;

my @pgcommands;
$result = $dbh->prepare( "SELECT * FROM trp_summary WHERE trp_summary ~ ':' AND trp_summary !~ '::' ORDER BY joinkey" );
$result->execute() or die "Cannot prepare statement: $DBI::errstr\n"; 
while (my @row = $result->fetchrow) {
  my ($joinkey, $olddata) = ($row[0], $row[1]);
  my $newdata = $olddata; $newdata =~ s/:/::/g;
  if ($newdata =~ m/\'/) { $newdata =~ s/\'/''/g; }
  push @pgcommands, "DELETE FROM trp_summary WHERE joinkey = '$joinkey';";
  push @pgcommands, "INSERT INTO trp_summary VALUES ('$joinkey', '$newdata');";
  push @pgcommands, "INSERT INTO trp_summary_hst VALUES ('$joinkey', '$newdata');";
}


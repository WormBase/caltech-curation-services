  push @pro_pgcommands, "INSERT INTO pro_remark VALUES ('$joinkey', '$remark');";
  push @pro_pgcommands, "INSERT INTO pro_remark_hst VALUES ('$joinkey', '$remark');";
}
close (IN) or die "Cannot close $infile : $!";
$/ = "\n";

foreach my $command (@pro_pgcommands) {
  print "$command\n";
